package com.example.buddyapp;

public class matchedProfile {

        private String userID;
        private String matchedProfileID;
        private String matchedRate;


    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getMatchedProfileID() {
        return matchedProfileID;
    }

    public void setMatchedProfileID(String matchedProfileID) {
        this.matchedProfileID = matchedProfileID;
    }

    public String getMatchedRate() {
        return matchedRate;
    }

    public void setMatchedRate(String matchedRate) {
        this.matchedRate = matchedRate;
    }
}
